// 当前语言
let currentLanguage = 'en';

// 获取翻译文本
function translate(key) {
    return translations[currentLanguage][key] || key;
}

// 切换语言
function changeLanguage() {
    const newLang = document.getElementById('language-select').value;
    if (newLang === currentLanguage) return; // 如果语言没变，不做任何操作

    currentLanguage = newLang;
    localStorage.setItem('language', currentLanguage);

    // 先更新页面文本
    updatePageText();

    // 重新渲染当前页面内容
    const currentPage = document.querySelector('.page.active');
    if (currentPage) {
        const pageName = currentPage.id.replace('page-', '');
        // 使用 setTimeout 延迟执行，避免与文本更新冲突
        setTimeout(() => {
            switch(pageName) {
                case 'products':
                    renderProducts();
                    break;
                case 'cart':
                    renderCart();
                    break;
                case 'orders':
                    renderOrders();
                    break;
                case 'checkout':
                    renderCheckout();
                    break;
                case 'profile':
                    renderProfile();
                    break;
            }
        }, 0);
    }
}

// 更新页面文本
function updatePageText() {
    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        const text = translate(key);
        if (text) {
            element.textContent = text;
        }
    });
    
    // 更新 placeholder 文本
    document.querySelectorAll('[data-translate-placeholder]').forEach(element => {
        const key = element.getAttribute('data-translate-placeholder');
        const text = translate(key);
        if (text) {
            element.placeholder = text;
        }
    });
}

// 初始化语言设置 - 简化版本，只读取保存的语言，不做任何DOM操作
function initLanguage() {
    const savedLanguage = localStorage.getItem('language');
    // 如果有保存的语言且不是英文，则更新语言变量
    if (savedLanguage && translations[savedLanguage] && savedLanguage !== 'en') {
        currentLanguage = savedLanguage;
        // 只更新下拉框的值，不调用 updatePageText()
        const langSelect = document.getElementById('language-select');
        if (langSelect) {
            langSelect.value = currentLanguage;
        }
        // 延迟更新文本，等页面完全渲染后再更新
        requestAnimationFrame(() => {
            setTimeout(updatePageText, 0);
        });
    }
    // 默认语言(en)不需要任何操作，HTML已经是英文
}
